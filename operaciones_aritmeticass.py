class OperacionesAritmeticas:
    def __init__(self, operando1, operando2):
        self.operando1 = operando1
        self.operando2 = operando2
    def calcular_Suma(self):
        return self.operando1 + self.operando2

    def calcular_division(self):
        return self.operando1 / self.operando2
